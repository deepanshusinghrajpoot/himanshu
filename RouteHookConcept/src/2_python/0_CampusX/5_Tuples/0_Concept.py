# Tuples
#========

# lll A tuple in Python is an ordered, immutable collection of elements
#     that can store multiple values of different data types.


# lll A tuple in Python is similar to a list,
#     but the main difference is:
#       👉 Tuple is immutable (cannot be changed)
#       👉 List is mutable (can be changed)
#
#     Therefore, we cannot perform adding, editing or deleting operations on a tuple.
#     We can only perform read operations.


# Characteristics of Tuple
#--------------------------

# lll Ordered                 -> Items have a fixed position
# lll Unchangeable            -> Cannot modify elements after creation
# lll Allows duplicate values



# Once a tuple is created, its elements cannot be modified, added, or removed.
# In short, a tuple is a read-only list.




