# Dictionary
#===========

# lll A dictionary in Python is an unordered, mutable collection of
#     key–value pairs that can store multiple values of different data types.

# lll A dictionary in Python is different from a list and tuple,
#     because:
#         👉 Dictionary stores data in key : value form
#         👉 Keys must be unique
#         👉 Dictionary is mutable (can be changed)
#
#     Therefore, we can perform adding, updating, deleting,
#     and reading operations on a dictionary.


# Characteristics of Dictionary
#-------------------------------

# lll Unordered               -> Items do not have a fixed position
# lll Mutable                 -> Values can be modified
# lll Stores data as key-value pairs
# lll Keys must be unique and immutable





# Definition
#------------
# Dictionary in Python is a collection of key-value pairs.
# Used to store data values like a map or associative array.
# Keys are unique and immutable, values can be anything.

# Example
d = {'name': 'Nitish', 'age': 33, 'gender': 'male'}
print(d)
# {'name': 'Nitish', 'age': 33, 'gender': 'male'}


# Characteristics (V.V.V. Important)
#-----------------------------------
# 1. Mutable → can add, edit, delete items
# 2. Unordered → indexing has no meaning
# 3. Keys must be unique → duplicates not allowed
# 4. Keys must be immutable → e.g., int, str, tuple
# 5. Values can be any type → mutable or immutable





