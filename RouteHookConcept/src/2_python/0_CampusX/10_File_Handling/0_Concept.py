# File I/O Theory
# ===============

# Types of Data used for I/O

# 1. Text:
#    Data stored as a sequence of Unicode characters.
#    Example: '12345'

# 2. Binary:
#    Data stored as a sequence of bytes (binary format).
#    Example: 1234 stored in binary form


# There are two types of files:

# 1. Text Files:
#    - Store data in readable format.
#    - All program source code files are text files.

# 2. Binary Files:
#    - Store data in binary (non-readable) format.
#    - Examples: image, music, video, executable (.exe) files.


# Basic Steps of File I/O in most programming languages:

# 1. Open a file
# 2. Read or Write data
# 3. Close the file
