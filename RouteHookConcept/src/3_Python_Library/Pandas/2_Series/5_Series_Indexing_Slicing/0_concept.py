'''


lll Pandas supports both position-based and label-based indexing.

   we can access data using different indexing techniques.

   1. Integer Indexing
   2. Label Indexing
   3. Slicing
   4. Fancy Indexing
   5. Label-based Fancy Indexing

Integer Indexing:
.................
It means accessing elements using position, starting from 0,
just like Python lists.


Label Indexing:
...............
Here, we access elements using custom index labels.
It works similar to dictionary key access and is very useful
in real-world datasets.


Slicing:
........
Slicing is used to access a range of elements using start, stop,
and step. It behaves like Python list slicing.


Fancy Indexing:
...............
In this, we pass a list of index positions to access multiple
elements at once.


Label-based Fancy Indexing:
...........................
Here, we pass a list of index labels instead of positions
to retrieve multiple values.


'''