# Definition (Interview Friendly)
# lll A DataFrame is a 2-dimensional labeled data structure.
# in Pandas that stores data in rows and columns,
# similar to a table, spreadsheet, or SQL table.


# DataFrame (table)
# ==================

# 2D structure of data consisting of rows and columns

# If a DataFrame has only one column (or one row), it can be
# represented as a Series.

# Each column of a DataFrame is called a Series.



# ==================================================
# Important Characteristics
# ==================================================

# • DataFrame is the most commonly used Pandas data structure
# • It is built on top of NumPy arrays
# • Columns can have different data types
# • Each column is internally stored as a Series
# • DataFrame has both row index and column labels



